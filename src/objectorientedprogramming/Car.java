package objectorientedprogramming;

public class Car {

	public Car()
	{
	}
	public void sayOdometer()
	{
		System.out.println("200,000 miles");
	}
	public void sayFlat()
	{
		System.out.println("My car has a flat tire.");
	}
	public void sayThirsty()
	{
		System.out.println("I need to go to a gas station, my car is out of gas.");
	}
}
