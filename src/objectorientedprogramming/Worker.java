package objectorientedprogramming;

public class Worker {
	//instance fields
	private int hours;
	private double pay;
	
	//constructors
	public Worker(){
		hours = 10;
		pay = 99.0;
	}
	public Worker(int newHours, double newPay){
		hours = newHours;
		pay = newPay;
	}
	// Accessor Methods
	
}
		

